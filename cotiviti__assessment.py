    sep = "─" * 72

    print(f"\n{SEP}")
    print("  COTIVITI POC — AGENTIC CLAIMS ANOMALY DETECTION DASHBOARD")
    print(f"  Analysis period: 26 weeks  |  Providers monitored: {len(claims)}")
