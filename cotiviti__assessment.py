    # Provider summary table
    print(f"\n{'PROVIDER':<36} {'TOTAL CLAIMS':>12} {'ANOMALIES':>10} {'MAX Z':>8}")
    print(sep)
    max_total = max(sum(p["count"] for p in d["weekly"]) for d in claims.values())
    for pid, data in claims.items():
        total = sum(p["count"] for p in data["weekly"])
        findings = [f for f in all_findings if f["provider_id"] == pid]
        max_z = max((f["z_score"] for f in findings), default=0.0)
        flag = "  ⚠ ALERT" if any(f["risk_level"] == "HIGH" for f in findings) else ""
        print(f"  {data['name']:<34} {total:>12,} {len(findings):>10} {max_z:>8.2f}{flag}")

    # Sparklines
    print(f"\n{'WEEKLY CLAIM TREND (26 weeks, left=Jan 2025)':}")
    print(sep)
    for pid, data in claims.items():
        spark = sparkline(data["weekly"], width=50)
        print(f"  {data['name'][:34]:<34} {spark}")

    # Detailed findings
    print(f"\n{SEP}")
    print("  ANOMALY FINDINGS — AGENTIC CLASSIFICATION RESULTS")
    print(SEP)

    if not all_findings:
        print("  No anomalies detected above threshold.\n")
    else:
        for f in sorted(all_findings, key=lambda x: -x["z_score"]):
            risk_label = {"HIGH": "[ HIGH RISK ]", "MEDIUM": "[ MED  RISK ]", "LOW": "[ LOW  RISK ]"}[f["risk_level"]]
            print(f"\n  {risk_label}  {f['provider_name']}")
            print(f"  Week: {f['week']}  |  Claims: {f['claim_count']:,}  |  Expected ~{f['expected_mean']:.0f}  |  Ratio: {f['ratio_to_mean']:.1f}x  |  Z={f['z_score']}")
            print(f"  ► Action: {f['recommended_action']}")
            print(f"  {'─'*66}")

